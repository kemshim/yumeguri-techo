// 温泉を検索する関数
export function searchOnsen(query: {
  keyword?: string;
  prefecture?: string;
  springQualityType?: string;
}): Onsen[] {
  let results = onsenData;

  if (query.keyword) {
    const keyword = query.keyword.toLowerCase();
    results = results.filter(
      (onsen) =>
        onsen.name.toLowerCase().includes(keyword) ||
        onsen.address.toLowerCase().includes(keyword)
    );
  }

  if (query.prefecture) {
    results = results.filter((onsen) => onsen.prefecture === query.prefecture);
  }

  if (query.springQualityType) {
    results = results.filter(
      (onsen) => onsen.springQualityType === query.springQualityType
    );
  }

  return results;
}

// IDで温泉を取得する関数
export function getOnsenById(id: string): Onsen | undefined {
  return onsenData.find((onsen) => onsen.id === id);
}
